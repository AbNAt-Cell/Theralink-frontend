// Template form definitions with field sections for each document type

export interface TemplateField {
    id: string;
    label: string;
    type: 'text' | 'textarea' | 'select' | 'radio' | 'checkbox' | 'number' | 'date' | 'rating';
    options?: { value: string; label: string }[];
    placeholder?: string;
    required?: boolean;
    hasNA?: boolean;
}

